    {
        //
        $request->validate([
            'content' => 'required',
        ]);
        $request->merge([
            'created_by' => Auth::user()->id
        ]);
        Comment::create($request->all());
        $idea = Idea::find($request->idea_id);
//        dd($idea);
        Mail::to($idea->createdBy->email)->send(new NotifyMail());

//        return redirect()->route('welcome');
        return redirect()->route('ideas.index');
    }

